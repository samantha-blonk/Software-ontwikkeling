#include "bitmap.h"

//--------------------------------------------------------------
// Lookup table
//--------------------------------------------------------------
uint8_t bmLookup[31];
uint16_t bitmap[31][17][16];
uint8_t i;
uint8_t j;

void Bitmap_init()
{
	//----------------------------------------------------------
	// Generate lookup table
	//----------------------------------------------------------
	for(i = 0; i < 31; i++)
	{	if(i < 26)
			bmLookup[i] = ('a' + i);
		if(i == 30)
			bmLookup[i] = 1;
	}


	//----------------------------------------------------------
	// Generate alphabet bitmap
	//----------------------------------------------------------

	//----------------------------------------------------------
	// Generate general bitmap
	//----------------------------------------------------------
	for(j = 0; j < 16; j++)
	{
		for(i = 0; i < 17; i++)
		{
			if(i == 16)
			{
				bitmap[30][i][j] = 0xFF00;
				if(j == 15)
					bitmap[30][i][j] = 0xFFFF;
			}
			else if(i == 7)
				bitmap[30][i][j] = VGA_COL_GREEN;
			else
				bitmap[30][i][j] = 0x0000;
		}
	}
	i++;
}

